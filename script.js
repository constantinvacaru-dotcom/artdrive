/* ===================== MOBILE MENU ===================== */
const menuToggle = document.getElementById('menuToggle');
const nav = document.getElementById('nav');
menuToggle.addEventListener('click', () => {
  const open = nav.classList.toggle('open');
  menuToggle.classList.toggle('open', open);
  menuToggle.setAttribute('aria-expanded', open);
});
nav.querySelectorAll('a').forEach(a => a.addEventListener('click', () => {
  nav.classList.remove('open'); menuToggle.classList.remove('open'); menuToggle.setAttribute('aria-expanded', false);
}));

/* ===================== GALLERY (placeholder fallback + lightbox) ===================== */
const lightbox = document.getElementById('lightbox');
const lightboxImg = document.getElementById('lightboxImg');
document.querySelectorAll('.gallery-item').forEach((item, i) => {
  const img = item.querySelector('img');
  // If real photo missing, show a styled placeholder instead of a broken image.
  img.addEventListener('error', () => {
    item.classList.add('placeholder');
    item.setAttribute('data-num', i + 1);
  });
  item.addEventListener('click', () => {
    if (item.classList.contains('placeholder')) return;
    lightboxImg.src = item.dataset.src;
    lightbox.classList.add('open');
    lightbox.setAttribute('aria-hidden', 'false');
  });
});
function closeLightbox() { lightbox.classList.remove('open'); lightbox.setAttribute('aria-hidden', 'true'); }
lightbox.addEventListener('click', e => { if (e.target === lightbox || e.target.classList.contains('lightbox-close')) closeLightbox(); });
document.addEventListener('keydown', e => { if (e.key === 'Escape') closeLightbox(); });

/* ===================== SCROLL REVEAL ===================== */
const revealEls = document.querySelectorAll('.card, .gallery-item, .why-list li, .why-highlight, .contact-line, .section-head');
revealEls.forEach(el => el.classList.add('reveal'));
const io = new IntersectionObserver((entries) => {
  entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('visible'); io.unobserve(e.target); } });
}, { threshold: 0.12 });
revealEls.forEach(el => io.observe(el));

